package com.am_lab.Model.DataModel;

public class JoystickModel {

    //region JoystickModel variables
    private String x;
    private String center;
    private String y;
    //endregion

    //region getters+setters
    public String getX() {
        return x;
    }
    public void setX(String x) {
        this.x = x;
    }
    public String getCenter() {
        return center;
    }
    public void setCenter(String center) {
        this.center = center;
    }
    public String getY() {
        return y;
    }
    public void setY(String y) {
        this.y = y;
    }
    //endregion


}
